    def print_test_report(self):
        """Imprime relat√≥rio final dos testes."""
        print("\n" + "="*80)
        print("üìä RELAT√ìRIO FINAL DOS TESTES")
        print("="*80)
        
        total_tests = self.test_results['passed'] + self.test_results['failed']
        success_rate = (self.test_results['passed'] / total_tests * 100) if total_tests > 0 else 0
        
        print(f"\n‚úÖ Testes aprovados: {self.test_results['passed']}")
        print(f"‚ùå Testes falhados: {self.test_results['failed']}")
        print(f"üìà Taxa de sucesso: {success_rate:.1f}%")
        
        if self.test_results['errors']:
            print(f"\n‚ö†Ô∏è Erros encontrados ({len(self.test_results['errors'])}):")
            for error in self.test_results['errors'][:10]:  # Mostra at√© 10 erros
                print(f"  ‚Ä¢ {error}")
        
        # Avalia√ß√£o final
        print(f"\nüèÜ AVALIA√á√ÉO FINAL: ", end="")
        if success_rate >= 90:
            print("EXCELENTE - Sistema est√° funcionando perfeitamente! ‚ú®")
        elif success_rate >= 70:
            print("BOM - Sistema funcional com pequenos ajustes necess√°rios üëç")
        elif success_rate >= 50:
            print("REGULAR - Sistema precisa de melhorias significativas üîß")
        else:
            print("CR√çTICO - Sistema com muitos problemas, requer aten√ß√£o urgente ‚ö†Ô∏è")
        
        print("\n" + "="*80)


